SELECT FUNCTION_HASH02('RI' || '|' || 'IFTB_CLT_MASTER' ,T.CLT_CODE  ) RI_ID,
		FUNCTION_HASH02 ('CL' ||'|'|| 'SRC_STM', 'IFTB.CLT_MASTER') SRC_STM_ID,
		FUNCTION_HASH02 ('CL' ||'|'|| 'IFTB.CCY', icmm.CCY_CODE) CCY_ID,
		icmm.CLT_VAL_LCY VAL_LCY,
		icmm.CLT_VAL_FCY VAL_FCY,
		#pdate# EFF_DT,
		99991231 END_DT,
		#pdate# PPN_DT,
		T.REC_IND REC_IND 
FROM FSSTRAINING.IFTB_CLT_MASTER_MNS T 
LEFT JOIN FSSTRAINING.IFTB_CLT_MASTER_TDY icmm
ON icmm.CLT_CODE  = T.CLT_CODE