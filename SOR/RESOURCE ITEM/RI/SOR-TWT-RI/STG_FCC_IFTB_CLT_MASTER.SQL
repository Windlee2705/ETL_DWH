SELECT FUNCTION_HASH02('RI' || '|' || 'IFTB_CLT_MASTER' ,mns.CLT_CODE  ) RI_ID,
		dty.CLT_CODE UNQ_ID_IN_SRC_STM,
		dty.MAT_DT PPN_DT,
		FUNCTION_HASH02('CL' || '|' || 'RI_TP' ,dty.CLT_TP_CODE  ) RI_TP_ID,
		MNS.REC_IND
FROM IFTB_CLT_MASTER_MNS mns
LEFT JOIN IFTB_CLT_MASTER_TDY dty ON dty.CLT_CODE = MNS.CLT_CODE