SELECT FUNCTION_HASH02('RI' || '|' || 'IFTB_CLT_MASTER' ,A.CLT_CODE  ) RI_ID,
		FUNCTION_HASH02('CL' || '|' || 'SRC_STM' ,'IFTB.CLT_MASTER'  ) SRC_STM_ID,
		FUNCTION_HASH02('CL' || '|' || 'RI_X_IP_TP', 'RI_X_CST') RI_X_IP_RLTNP_TP_ID,
		FUNCTION_HASH02('IP' || '|' || 'IFTB.CUSTOMER' ,icm.OWNER_ID) IP_ID ,
		#pdate# EFF_DT,
		99991231 END_DT,
		#pdate# PPN_DT,
		A.REC_IND
FROM IFTB_CLT_MASTER_MNS A
LEFT JOIN FSSTRAINING.IFTB_CLT_MASTER_TDY icm ON A.CLT_CODE = icm.CLT_CODE;